import java.util.Scanner;

public class CinemaRoom {

    private char[][] cinemaArray;
    private int rows;
    private int seats;
    private int rowNum;
    private int seatNum;

    public char[][] getCinemaArray() {
        return cinemaArray;
    }

    public int getRows() {
        return rows;
    }

    public int getSeats() {
        return seats;
    }

    public int getRowNum() {
        return rowNum;
    }

    public int getSeatNum() {
        return seatNum;
    }

}
